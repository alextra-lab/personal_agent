#!/usr/bin/env python3
"""FRE-277 — Eval data cleanup script.

Purges eval-run sessions, traces, costs, Captain's Log captures and reflections,
and Elasticsearch log events that were generated by eval runs against the prod stack.

Usage
-----
    uv run python scripts/cleanup_eval_data.py \\
        telemetry/evaluation/EVAL-primitive-tools/run-*/results.json \\
        [--dry-run]

Notes
-----
- Reads session_id and trace_id from each results.json (control + treatment sides).
- Deletes rows in FK-safe order: reflections → captures → api_costs → sessions.
- Archives Captain's Log JSON files to telemetry/captains_log/archive/eval/<date>/.
- Deletes Elasticsearch documents via _delete_by_query on agent-logs-*.
"""

from __future__ import annotations

import argparse
import asyncio
import glob
import json
import pathlib
import re
import shutil
from datetime import date
from typing import Any

_UUID_RE = re.compile(
    r"^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$",
    re.IGNORECASE,
)

import httpx
import structlog

log = structlog.get_logger(__name__)

_PROJECT_ROOT = pathlib.Path(__file__).resolve().parents[1]
_DEFAULT_CAPTURES_ROOT = _PROJECT_ROOT / "telemetry" / "captains_log" / "captures"
_DEFAULT_ARCHIVE_ROOT = _PROJECT_ROOT / "telemetry" / "captains_log" / "archive" / "eval"


# ── Pure functions (testable without I/O) ─────────────────────────────────────


def parse_results_files(
    paths: list[pathlib.Path],
) -> tuple[set[str], set[str]]:
    """Extract unique session_ids and trace_ids from a list of results.json files.

    Args:
        paths: Paths to results.json files produced by run_primitive_tools_eval.py.

    Returns:
        Tuple of (session_ids, trace_ids) as sets of strings.
    """
    session_ids: set[str] = set()
    trace_ids: set[str] = set()
    for p in paths:
        try:
            data = json.loads(p.read_text())
        except (OSError, json.JSONDecodeError) as exc:
            log.warning("results_file_unreadable", path=str(p), error=str(exc))
            continue
        for result in data:
            for side in ("control", "treatment"):
                entry = result.get(side, {})
                if sid := entry.get("session_id"):
                    if _UUID_RE.match(sid):
                        session_ids.add(sid)
                if tid := entry.get("trace_id"):
                    if _UUID_RE.match(tid):
                        trace_ids.add(tid)
    return session_ids, trace_ids


def archive_capture_files(
    trace_ids: set[str],
    captures_root: pathlib.Path,
    archive_root: pathlib.Path,
    dry_run: bool = False,
) -> int:
    """Move Captain's Log capture JSON files matching trace_ids to archive.

    Args:
        trace_ids: Set of trace_id strings to archive.
        captures_root: Root of captures directory (contains date-named subdirs).
        archive_root: Destination archive directory.
        dry_run: If True, count matches but do not move files.

    Returns:
        Number of files moved (or that would be moved in dry_run mode).
    """
    moved = 0
    for capture_file in captures_root.rglob("*.json"):
        stem = capture_file.stem
        if stem not in trace_ids:
            continue
        if not dry_run:
            dest_dir = archive_root / date.today().isoformat()
            dest_dir.mkdir(parents=True, exist_ok=True)
            shutil.move(str(capture_file), str(dest_dir / capture_file.name))
        moved += 1
    return moved


# ── I/O: PostgreSQL ───────────────────────────────────────────────────────────


async def resolve_session_ids_from_es(
    trace_ids: set[str],
    es_url: str,
) -> set[str]:
    """Look up actual database session_ids from trace_ids via Elasticsearch.

    The results.json session_id is a synthetic UUID generated by the eval runner
    (uuid5 from prompt prefix), not the real database session_id. Trace_ids ARE
    real — look them up in ES to find the corresponding session_ids.

    Uses the _search terms query (not ES|QL) for reliable multi-value lookup.

    Args:
        trace_ids: Real trace UUIDs from results.json.
        es_url: Elasticsearch base URL.

    Returns:
        Set of actual database session_id UUIDs.
    """
    if not trace_ids:
        return set()

    session_ids: set[str] = set()
    async with httpx.AsyncClient(base_url=es_url, timeout=60.0) as client:
        resp = await client.post(
            "/agent-logs-*/_search",
            json={
                "size": 0,
                "query": {"terms": {"trace_id.keyword": list(trace_ids)}},
                "aggs": {
                    "by_trace": {
                        "terms": {"field": "trace_id.keyword", "size": len(trace_ids) + 10},
                        "aggs": {
                            "session": {"terms": {"field": "session_id.keyword", "size": 1}}
                        },
                    }
                },
            },
            headers={"Content-Type": "application/json"},
        )
        resp.raise_for_status()
        data = resp.json()
        for bucket in data.get("aggregations", {}).get("by_trace", {}).get("buckets", []):
            for sb in bucket.get("session", {}).get("buckets", []):
                sid = sb.get("key", "")
                if _UUID_RE.match(str(sid)):
                    session_ids.add(str(sid))
    return session_ids


async def purge_postgres(
    session_ids: set[str],
    trace_ids: set[str],
    db_url: str,
    dry_run: bool = False,
) -> dict[str, int]:
    """Delete eval rows from PostgreSQL in FK-safe order.

    Args:
        session_ids: Real database session UUIDs (resolved from ES, not from results.json).
        trace_ids: Trace UUIDs to delete.
        db_url: asyncpg-compatible connection URL.
        dry_run: If True, COUNT rows without deleting.

    Returns:
        Dict mapping table name → rows affected.
    """
    import asyncpg  # noqa: PLC0415 — optional dep; only needed at runtime

    # Strip SQLAlchemy dialect prefix that asyncpg doesn't understand
    raw_url = db_url.replace("postgresql+asyncpg://", "postgresql://")
    conn = await asyncpg.connect(raw_url)
    counts: dict[str, int] = {}
    try:
        sid_list = list(session_ids)
        tid_list = list(trace_ids)

        for table, col, values in [
            ("captains_log_reflections", "trace_id", tid_list),
            ("captains_log_captures",    "trace_id", tid_list),
            ("api_costs",                "trace_id", tid_list),
            ("sessions",                 "session_id", sid_list),
        ]:
            if not values:
                counts[table] = 0
                continue
            if dry_run:
                row = await conn.fetchrow(
                    f"SELECT count(*) AS n FROM {table} WHERE {col} = ANY($1::uuid[])",  # noqa: S608
                    values,
                )
                counts[table] = row["n"]
            else:
                result = await conn.execute(
                    f"DELETE FROM {table} WHERE {col} = ANY($1::uuid[])",  # noqa: S608
                    values,
                )
                counts[table] = int(result.split()[-1])
    finally:
        await conn.close()
    return counts


# ── I/O: Elasticsearch ────────────────────────────────────────────────────────


async def purge_elasticsearch(
    session_ids: set[str],
    es_url: str,
    dry_run: bool = False,
) -> int:
    """Delete agent-logs-* documents matching eval session_ids.

    Args:
        session_ids: Session UUIDs whose log documents should be removed.
        es_url: Elasticsearch base URL.
        dry_run: If True, count matching docs without deleting.

    Returns:
        Number of documents deleted (or that would be deleted).
    """
    if not session_ids:
        return 0

    query: dict[str, Any] = {
        "query": {"terms": {"session_id.keyword": list(session_ids)}}
    }

    async with httpx.AsyncClient(base_url=es_url, timeout=120.0) as client:
        if dry_run:
            resp = await client.post(
                "/agent-logs-*/_count",
                json=query,
                headers={"Content-Type": "application/json"},
            )
            resp.raise_for_status()
            return resp.json().get("count", 0)
        else:
            resp = await client.post(
                "/agent-logs-*/_delete_by_query?conflicts=proceed&wait_for_completion=true",
                json=query,
                headers={"Content-Type": "application/json"},
            )
            resp.raise_for_status()
            return resp.json().get("deleted", 0)


# ── Main ──────────────────────────────────────────────────────────────────────


async def main(args: argparse.Namespace) -> None:
    """Run the cleanup pipeline."""
    # Resolve glob patterns from CLI (argparse doesn't expand shell globs when quoted)
    paths: list[pathlib.Path] = []
    for pattern in args.results:
        expanded = glob.glob(pattern)
        if expanded:
            paths.extend(pathlib.Path(p) for p in sorted(expanded))
        else:
            paths.append(pathlib.Path(pattern))

    if not paths:
        print("No results.json files found.")
        return

    print(f"{'DRY RUN — ' if args.dry_run else ''}Scanning {len(paths)} results file(s)...")
    session_ids, trace_ids = parse_results_files(paths)
    print(f"  Sessions to purge: {len(session_ids)}")
    print(f"  Traces  to purge:  {len(trace_ids)}")

    if not session_ids and not trace_ids:
        print("Nothing to clean up.")
        return

    from personal_agent.config import settings  # noqa: PLC0415

    # Resolve real DB session_ids from ES (results.json session_ids are synthetic)
    es_url = args.es_url or str(settings.elasticsearch_url)
    print(f"\nResolving session_ids from ES ({es_url})...")
    real_session_ids = await resolve_session_ids_from_es(trace_ids, es_url)
    print(f"  Resolved {len(real_session_ids)} real database session_ids from {len(trace_ids)} traces")

    # PostgreSQL
    db_url = args.db_url or str(settings.database_url)
    print(f"\nPostgreSQL ({db_url[:40]}...):")
    pg_counts = await purge_postgres(real_session_ids, trace_ids, db_url, dry_run=args.dry_run)
    for table, n in pg_counts.items():
        verb = "Would delete" if args.dry_run else "Deleted"
        print(f"  {verb} {n:4d} rows from {table}")

    # Elasticsearch (es_url already resolved above)
    print(f"\nElasticsearch ({es_url}):")
    es_deleted = await purge_elasticsearch(real_session_ids, es_url, dry_run=args.dry_run)
    verb = "Would delete" if args.dry_run else "Deleted"
    print(f"  {verb} {es_deleted:4d} documents from agent-logs-*")

    # Captain's Log file archive
    captures_root = pathlib.Path(args.captures_root) if args.captures_root else _DEFAULT_CAPTURES_ROOT
    archive_root = pathlib.Path(args.archive_dir) if args.archive_dir else _DEFAULT_ARCHIVE_ROOT
    print(f"\nCaptain's Log captures ({captures_root}):")
    moved = archive_capture_files(trace_ids, captures_root, archive_root, dry_run=args.dry_run)
    verb = "Would archive" if args.dry_run else "Archived"
    print(f"  {verb} {moved:4d} capture JSON files → {archive_root}")

    if args.dry_run:
        print("\n✓ Dry run complete — no data was modified.")
    else:
        print("\n✓ Cleanup complete.")


def parse_args() -> argparse.Namespace:
    """Parse command-line arguments."""
    parser = argparse.ArgumentParser(
        description="Purge eval-run data from PostgreSQL, Elasticsearch, and Captain's Log.",
        epilog=(
            "Example:\n"
            "  uv run python scripts/cleanup_eval_data.py \\\n"
            '    "telemetry/evaluation/EVAL-primitive-tools/run-*/results.json" \\\n'
            "    --dry-run"
        ),
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument(
        "results",
        nargs="+",
        metavar="RESULTS_JSON",
        help="Path(s) or glob pattern(s) to results.json files from eval runs",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print counts without deleting anything",
    )
    parser.add_argument(
        "--db-url",
        default=None,
        help="PostgreSQL connection URL (default: AGENT_DATABASE_URL from settings)",
    )
    parser.add_argument(
        "--es-url",
        default=None,
        help="Elasticsearch base URL (default: AGENT_ELASTICSEARCH_URL from settings)",
    )
    parser.add_argument(
        "--captures-root",
        default=None,
        help=f"Captain's Log captures root (default: {_DEFAULT_CAPTURES_ROOT})",
    )
    parser.add_argument(
        "--archive-dir",
        default=None,
        help=f"Archive destination for capture files (default: {_DEFAULT_ARCHIVE_ROOT})",
    )
    return parser.parse_args()


if __name__ == "__main__":
    import os
    os.chdir(_PROJECT_ROOT)
    asyncio.run(main(parse_args()))
